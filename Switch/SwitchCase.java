public class SwitchCase {
    public static void main(String[] args){
        char x = 'C';
        
        switch(x){
            case 'A':
                System.out.println("Range of marks 80 - 100");
                break;
            case 'B':
                System.out.println("Range of marks 73 - 79");
                break;
                case 'C':
                System.out.println("Range of marks 65 - 72");
                break;
            case 'D':
                System.out.println("Range of marks 80 - 100");
                break;
            case 'E':
                System.out.println("Range of marks 80 - 100");
                break;
        }
    }
    
}
